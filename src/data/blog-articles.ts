export function getArticleBySlug(
  slug: string
): BlogArticle | undefined {
  return blogArticles.find((a) => a.slug === slug);
}

export function slugifyCategory(category: string): string {
  return category
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "");
}

export interface CategoryInfo {
  name: string;
  slug: string;
  count: number;
}

export function getCategories(): CategoryInfo[] {
  const categoryMap = new Map<string, number>();
  for (const article of blogArticles) {
    categoryMap.set(article.category, (categoryMap.get(article.category) || 0) + 1);
  }
  return Array.from(categoryMap.entries()).map(([name, count]) => ({
    name,
    slug: slugifyCategory(name),
    count,
  }));
}

export function getArticlesByCategory(categorySlug: string): BlogArticle[] {
  return blogArticles.filter(
    (a) => slugifyCategory(a.category) === categorySlug
  );
}

export function getCategoryNameBySlug(categorySlug: string): string | undefined {
  const article = blogArticles.find(
    (a) => slugifyCategory(a.category) === categorySlug
  );
  return article?.category;
}

export function getRelatedArticles(
  currentSlug: string,
  count: number = 3
): BlogArticle[] {
  const current = getArticleBySlug(currentSlug);
  if (!current) return blogArticles.slice(0, count);

  // Prioritize same category, then different
  const sameCategory = blogArticles.filter(
    (a) => a.slug !== currentSlug && a.category === current.category
  );
  const otherCategory = blogArticles.filter(
    (a) => a.slug !== currentSlug && a.category !== current.category
  );

  return [...sameCategory, ...otherCategory].slice(0, count);
}
